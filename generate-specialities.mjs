/**
 * Genera combos de especialidades por localidad:
 *  - data/valid-provincia-specs.json   (provincia/especialidad)
 *  - data/valid-municipio-specs.json   (municipio/especialidad)
 *  - data/valid-specialities.json      (especialidades únicas)
 *
 * Lee data/valid-municipios.json (generado por generate-municipios.mjs).
 * Uso: node generate-specialities.mjs
 */

import { readFileSync, writeFileSync, existsSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));

const ASISA_API_KEY = '0908b85b9d0e4a75b2eb33048bd9fe01';
const ASISA_BASE = 'https://ursaepre.asisa.es/ASISA/middlewasisa/public/v1/api/searchPortal';

function normalize(str) {
  return str.normalize('NFD').replace(/[\u0300-\u036f]/g, '').toLowerCase().trim();
}

function toSlug(str) {
  return normalize(str).replace(/\s+/g, '-');
}

async function asisaFetch(url) {
  const controller = new AbortController();
  const timeout = setTimeout(() => controller.abort(), 15000);
  try {
    const resp = await fetch(url, {
      signal: controller.signal,
      headers: { 'Ocp-Apim-Subscription-Key': ASISA_API_KEY, 'Api-Version': '1' },
    });
    clearTimeout(timeout);
    if (!resp.ok) return null;
    return resp.json();
  } catch {
    clearTimeout(timeout);
    return null;
  }
}

async function fetchSpecialities(provinceCode) {
  const url = `${ASISA_BASE}/autocomplete/specialities?specialityDescription=&networkCode=1&provinceCode=${provinceCode}&maxResultsNumber=200`;
  return (await asisaFetch(url)) || [];
}

async function main() {
  const provincias = JSON.parse(readFileSync(join(__dirname, 'data/provincias.json'), 'utf8'));

  // Load valid municipios (generated by generate-municipios.mjs)
  const validMuniPath = join(__dirname, 'data/valid-municipios.json');
  const validMunicipios = existsSync(validMuniPath)
    ? new Set(JSON.parse(readFileSync(validMuniPath, 'utf8')))
    : new Set();

  // Build municipio → provinceCode map
  const municipios = JSON.parse(readFileSync(join(__dirname, 'data/municipios.json'), 'utf8'));
  const muniProvMap = new Map();
  for (const m of municipios) {
    muniProvMap.set(m.municipio, m.provinceCode);
  }

  const provinciaSpecs = new Set();
  const municipioSpecs = new Set();
  const allSpecs = new Set();
  let processed = 0;

  for (const prov of provincias) {
    processed += 1;
    const provSlug = `provincia-de-${toSlug(prov.name)}`;
    console.log(`[${processed}/${provincias.length}] ${prov.name}...`);

    const specialities = await fetchSpecialities(prov.provinceCode);
    if (specialities.length === 0) { console.log('  Sin especialidades'); continue; }

    for (const spec of specialities) {
      const specSlug = toSlug(spec.specialityDescription);
      allSpecs.add(specSlug);
      provinciaSpecs.add(`${provSlug}/${specSlug}`);
    }

    console.log(`  ${specialities.length} especialidades`);
  }

  // Cross valid municipios with all specialities from their province
  for (const muniName of validMunicipios) {
    const provCode = muniProvMap.get(muniName);
    if (!provCode) continue;

    const prov = provincias.find((p) => p.provinceCode === provCode);
    if (!prov) continue;

    const provSlug = `provincia-de-${toSlug(prov.name)}`;
    const muniSlug = toSlug(muniName);

    // Get specialities already collected for this province
    for (const combo of provinciaSpecs) {
      if (combo.startsWith(`${provSlug}/`)) {
        const specSlug = combo.slice(provSlug.length + 1);
        municipioSpecs.add(`${muniSlug}/${specSlug}`);
      }
    }
  }

  const provSpecList = [...provinciaSpecs].sort();
  const muniSpecList = [...municipioSpecs].sort();
  const specList = [...allSpecs].sort();

  writeFileSync(join(__dirname, 'data/valid-provincia-specs.json'), JSON.stringify(provSpecList, null, 2), 'utf8');
  writeFileSync(join(__dirname, 'data/valid-municipio-specs.json'), JSON.stringify(muniSpecList, null, 2), 'utf8');
  writeFileSync(join(__dirname, 'data/valid-specialities.json'), JSON.stringify(specList, null, 2), 'utf8');

  console.log('\nGenerados:');
  console.log(`  valid-provincia-specs.json: ${provSpecList.length} combos`);
  console.log(`  valid-municipio-specs.json: ${muniSpecList.length} combos`);
  console.log(`  valid-specialities.json: ${specList.length} especialidades`);
}

main().catch((err) => { console.error('Error:', err); process.exit(1); });
